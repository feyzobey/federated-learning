int main(void)
{
  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();

  /* Initialize reception buffer */
  memset(rx_buffer, 0, RX_BUFFER_SIZE);

  // Print startup message
  char msg[] = "STM32 Device Receiver Started\r\n";
  HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 1000);

  /* Infinite loop */
  while (1)
  {
    /* Receive data via UART */
    if (HAL_UART_Receive(&huart2, rx_buffer, RX_BUFFER_SIZE, HAL_MAX_DELAY) == HAL_OK)
    {
      /* Process received data if it starts with MODEL_UPDATE marker */
      if (strncmp((char *)rx_buffer, "MODEL_UPDATE", 11) == 0)
      {
        /* Print the received model parameters */
        print_received_parameters();
        
        /* Clear buffer for next reception */
        memset(rx_buffer, 0, RX_BUFFER_SIZE);

        /* Toggle LED to indicate successful reception */
        HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
      }
    }
  }
}

void print_received_parameters(void)
{
  char line_buffer[128];
  
  // Print header
  sprintf(line_buffer, "\r\n--- Received Model Parameters ---\r\n");
  HAL_UART_Transmit(&huart2, (uint8_t*)line_buffer, strlen(line_buffer), 1000);
  
  // Print raw data in chunks
  char *data_ptr = (char*)rx_buffer;
  char *next_line;
  
  while ((next_line = strchr(data_ptr, '\n')) != NULL) {
    size_t line_len = next_line - data_ptr;
    
    // Cap line length to avoid buffer overflow
    if (line_len > 120) {
      line_len = 120;
    }
    
    // Copy this line to the buffer
    memcpy(line_buffer, data_ptr, line_len);
    line_buffer[line_len] = '\r';
    line_buffer[line_len+1] = '\n';
    line_buffer[line_len+2] = '\0';
    
    // Skip MODEL_UPDATE and END lines
    if (strncmp(line_buffer, "MODEL_UPDATE", 11) != 0 && 
        strncmp(line_buffer, "END", 3) != 0) {
      // Print the line
      HAL_UART_Transmit(&huart2, (uint8_t*)line_buffer, strlen(line_buffer), 1000);
    }
    
    // Move to next line
    data_ptr = next_line + 1;
  }
  
  // Print footer
  sprintf(line_buffer, "--- End of Parameters ---\r\n");
  HAL_UART_Transmit(&huart2, (uint8_t*)line_buffer, strlen(line_buffer), 1000);
}
